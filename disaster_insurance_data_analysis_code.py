import statsmodels.api as sm

for year in [2020, 2021, 2022, 2023]:
    data = merged[merged['Year'] == year].dropna(subset=['fire_count', 'Vol_NonrenewalRate'])

    if data.empty:
        print(f"No data available for year {year} after filtering.")
        continue

    X = data[['fire_count']]
    y = data['Vol_NonrenewalRate']
    X = sm.add_constant(X)

    model = sm.OLS(y, X).fit()

    print(f"\nRegression Results for {year}:")
    print(model.summary())

